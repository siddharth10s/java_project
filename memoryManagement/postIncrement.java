package memoryManagement;

public class postIncrement {
    public static void main(String[] args) {
        
        int x=2;
        //post increment
        System.out.println(x++);
        System.out.println(x);

        int y=5;
        //pre increment
        System.out.println(y);
        System.out.println(++y);
        
    }
}
